function buildPlanContent(scenario: EvalScenario): string {
  const f = scenario.fixture;
  const lines: string[] = [];

  lines.push(`# ${scenario.name}`);
  lines.push("");
  lines.push("## Context");
  lines.push(f.context ?? "");
  lines.push("");
  lines.push("## Objective");
  lines.push(f.objective ?? "");
  lines.push("");
  lines.push("## Decisions");
  if (f.decisions) {
    for (const d of f.decisions) lines.push(d);
  }
  lines.push("");
  lines.push("## Test Scenarios");
  lines.push("");
  lines.push("## Manual Tests");
  lines.push("");
  lines.push("## Implementation");
  lines.push("");
  lines.push("## Verification");
  lines.push("");
  lines.push("## Target Branch");
  lines.push(f.targetBranch);
  lines.push("");
  lines.push("## Branch");
  lines.push(f.branch);
  lines.push("");
  lines.push("## Status");
  lines.push(f.planStatus);
  lines.push("");
  lines.push("## Progress");
  if (f.progressEntries) {
    for (const entry of f.progressEntries) {
      lines.push(`- ${entry}`);
    }
  }
  lines.push("");

  return lines.join("\n");
}

function buildClaudeLocalMd(planPath: string, scenario: EvalScenario): string {
  const f = scenario.fixture;
  return `## Worktree Session

**Plan file:** ${planPath}
**Branch:** ${f.branch} → ${f.targetBranch}

### Session Protocol

1. Read the plan file at session start
2. Append to Progress at start and end of each session
3. If status is \`pending\`, start planning. If \`in-progress\`, continue from last progress entry.
4. ALL change requests must be logged in the plan's Progress section, even when outside plan mode

### Planning Methodology

Key rules:

- Every plan requires **Context** (why) and **Objective** (what)
- Every plan must include a **Verification** section
- Testable plans must include **Test Scenarios** using AAA format (Arrange, Act, Assert)
- Architectural decisions require an ADR-first workflow: write the ADR in DECISIONS.md before implementing
`;
}

function buildSessionMd(planPath: string): string {
  return `# Session Context (auto-generated by claudet)

Read the plan file at ${planPath} at session start.
`;
}

export function setupEvalFixture(scenario: EvalScenario): EvalFixturePaths {
  const tmpDir = join(TEST_TMP, scenario.name);
  const sandboxHome = join(tmpDir, "home");
  const wtDir = join(tmpDir, "worktree");
  const planPath = join(tmpDir, "plan.md");
  const claudeDir = join(wtDir, ".claude");
  const rulesDir = join(claudeDir, "rules");
  const claudeLocalMd = join(claudeDir, "CLAUDE.local.md");

  // Clean any previous run
  rmSync(tmpDir, { recursive: true, force: true });

  mkdirSync(sandboxHome, { recursive: true });
  mkdirSync(rulesDir, { recursive: true });

  // Write plan file
  writeFileSync(planPath, buildPlanContent(scenario));

  // Write .claude/CLAUDE.local.md
  writeFileSync(claudeLocalMd, buildClaudeLocalMd(planPath, scenario));

  // Write .claude/rules/session.md
  writeFileSync(join(rulesDir, "session.md"), buildSessionMd(planPath));

  // Initialize a bare git repo so Claude doesn't complain
  try {
    execSync("git init -q", { cwd: wtDir, stdio: "pipe" });
  } catch {
    // non-critical — continue without git
  }

  return { tmpDir, wtDir, planPath, claudeLocalMd, sandboxHome };
}

// ---------------------------------------------------------------------------
// Claude invocation (subject under test)
// ---------------------------------------------------------------------------

export async function runSubject(
  fixture: EvalFixturePaths,
  prompt: string,
  opts?: { allowedTools?: string[]; maxBudget?: number },
): Promise<SubjectResult> {
  const tools = (opts?.allowedTools ?? ["Read", "Glob", "Grep"]).join(",");
  const maxBudget = opts?.maxBudget ?? 0.5;

  const args = [
    "-p",
    prompt,
    "--output-format",
    "json",
    "--max-budget-usd",
    String(maxBudget),
    "--no-session-persistence",
    "--allowedTools",
    tools,
  ];

  const out = await execFileAsync("claude", args, {
    cwd: fixture.wtDir,
    timeout: 240_000,
    env: { ...process.env, HOME: fixture.sandboxHome },
  });

  const parsed = JSON.parse(out);
  return {
    result: parsed.result ?? "",
    cost: parsed.cost_usd ?? 0,
    duration: parsed.duration_ms ?? 0,
  };
}

