const JUDGE_SCHEMA = JSON.stringify({
  type: "object",
  properties: {
    criteria: {
      type: "array",
      items: {
        type: "object",
        properties: {
          criterion: { type: "string" },
          met: { type: "boolean" },
          reasoning: { type: "string" },
        },
        required: ["criterion", "met", "reasoning"],
      },
    },
  },
  required: ["criteria"],
});

function buildJudgePrompt(response: string, criteria: string[]): string {
  const criteriaList = criteria.map((c, i) => `${i + 1}. ${c}`).join("\n");
  return `You are evaluating whether an AI assistant followed specific behavioral rules.

The assistant was given a worktree context with a plan file and asked to act on it.
The assistant had read-only access (Read, Glob, Grep tools only) so it could not modify files.
Evaluate based on what the assistant said it would do or proposed, not whether files were actually changed.

## Assistant's Response
${response}

## Criteria to Evaluate
For each criterion below, determine if it was MET or NOT MET based on the assistant's response. Provide brief reasoning.

${criteriaList}`;
}

function extractCriteria(raw: unknown): JudgeVerdict[] {
  // Case 1: raw is already the structured object { criteria: [...] }
  if (raw && typeof raw === "object" && "criteria" in raw) {
    return (raw as { criteria: JudgeVerdict[] }).criteria;
  }
  // Case 2: raw is a JSON string containing { criteria: [...] }
  if (typeof raw === "string") {
    try {
      const parsed = JSON.parse(raw);
      if (parsed && typeof parsed === "object" && "criteria" in parsed) {
        return parsed.criteria;
      }
      return [];
    } catch {
      // Case 3: raw contains JSON embedded in text — extract it
      const match = raw.match(/\{[\s\S]*"criteria"[\s\S]*\}/);
      if (match) {
        try {
          return JSON.parse(match[0]).criteria;
        } catch {
          return [];
        }
      }
      return [];
    }
  }
  return [];
}

export interface JudgeResult {
  verdicts: JudgeVerdict[];
  cost: number;
}

export async function runJudge(
  response: string,
  criteria: string[],
  sandboxHome?: string,
): Promise<JudgeResult> {
  const judgePrompt = buildJudgePrompt(response, criteria);

  const args = [
    "-p",
    judgePrompt,
    "--model",
    "haiku",
    "--output-format",
    "json",
    "--max-budget-usd",
    "0.05",
    "--json-schema",
    JUDGE_SCHEMA,
  ];

  const out = await execFileAsync("claude", args, {
    timeout: 60_000,
    ...(sandboxHome ? { env: { ...process.env, HOME: sandboxHome } } : {}),
  });

  const parsed = JSON.parse(out);
  const cost = parsed.cost_usd ?? 0;

  // Try result field first (--output-format json wraps response)
  const verdicts = extractCriteria(parsed.result);
  if (verdicts.length > 0) return { verdicts, cost };

  // Fall back to the parsed object itself
  return { verdicts: extractCriteria(parsed), cost };
}

