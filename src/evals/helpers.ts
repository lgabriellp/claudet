import { execFile, execSync } from "child_process";
import { mkdirSync, rmSync, writeFileSync } from "fs";
import { join } from "path";
import type { EvalScenario } from "./scenarios.js";

export interface EvalFixturePaths {
  tmpDir: string;
  wtDir: string;
  planPath: string;
  claudeLocalMd: string;
  sandboxHome: string;
}

interface SubjectResult {
  result: string;
  cost: number;
  duration: number;
}

interface JudgeVerdict {
  criterion: string;
  met: boolean;
  reasoning: string;
}

const TEST_TMP = join(import.meta.dirname!, "..", "..", ".test-tmp", "evals");

// ---------------------------------------------------------------------------
// Claude availability check
// ---------------------------------------------------------------------------

let _claudeAvailable: boolean | null = null;

export function isClaudeAvailable(): boolean {
  if (_claudeAvailable !== null) return _claudeAvailable;
  try {
    execSync("claude --version", { stdio: "pipe", timeout: 10_000 });
    _claudeAvailable = true;
  } catch {
    _claudeAvailable = false;
  }
  return _claudeAvailable;
}

// ---------------------------------------------------------------------------
// Async execFile helper
// ---------------------------------------------------------------------------

function execFileAsync(
  cmd: string,
  args: string[],
  opts: { cwd?: string; timeout?: number; env?: NodeJS.ProcessEnv },
): Promise<string> {
  return new Promise((resolve, reject) => {
    execFile(
      cmd,
      args,
      {
        cwd: opts.cwd,
        env: opts.env,
        encoding: "utf-8",
        timeout: opts.timeout,
        maxBuffer: 10 * 1024 * 1024,
      },
      (err, stdout) => {
        if (err) reject(err);
        else resolve(stdout);
      },
    );
  });
}

